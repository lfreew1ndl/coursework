package com.example.demo.model;

public class Region {

  private long id = 0;
  private String name;

  public Region() {
  }

  public Region(long id, String name) {
    this.id = id;
    this.name = name;
  }

  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
